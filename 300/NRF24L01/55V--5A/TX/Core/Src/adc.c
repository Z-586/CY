/********************************************************************************************************
**函数信息 ：Get_Adc(u8 ch) 
**功能描述 ：获得ADC值, ch:通道值 0~3
**输入参数 ：ch, ch为0~8
**输出参数 ：puiADData为ADC读到的值
********************************************************************************************************/
static uint16_t Get_Adc(unsigned long ch)   
{
	if(ch == ADC_CHANNEL_15){
		HAL_ADC_Start(&hadc2);
		if(HAL_OK == HAL_ADC_PollForConversion(&hadc2,100)){
			return HAL_ADC_GetValue(&hadc2);
		}else{
			return 0;
		}
	}else{
		ADC_ChannelConfTypeDef sConfig = {0};
		sConfig.Channel = ch;
		sConfig.Rank = ADC_REGULAR_RANK_1;
		sConfig.SamplingTime = ADC_SAMPLETIME_12CYCLES_5;
		sConfig.SingleDiff = ADC_SINGLE_ENDED;
		sConfig.OffsetNumber = ADC_OFFSET_NONE;
		sConfig.Offset = 0;
		if (HAL_ADC_ConfigChannel(&hadc1, &sConfig) != HAL_OK)
		{
			Error_Handler();
		}
		HAL_ADC_Start(&hadc1);
		if(HAL_OK == HAL_ADC_PollForConversion(&hadc1,100)){
			return HAL_ADC_GetValue(&hadc1);
		}else{
			return 0;
		}
	}
}

uint32_t Get_Adc_Average(unsigned long ch, uint16_t times)
{
	uint32_t temp_val  = 0;
	uint32_t total_val = 0;
	uint16_t Date_val = 0;
	uint8_t  time = 0;
	
	for(uint16_t t = 0; t < times; t++) {
		Date_val = Get_Adc(ch);
		if(Date_val == 0){
				time += 1;
		}else{
				temp_val += Date_val;
		}
		vTaskDelay(1);
	}
	total_val = temp_val/(times-time);
	temp_val = total_val;
	//temp_val = (total_val*3300)/4096;
	
	return temp_val;
} 	 


